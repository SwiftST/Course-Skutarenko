// –°–≤–æ–π—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫–∏ utf16 –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –Ω–∞–±–æ—Ä–∞ UTF-16 –µ–¥–∏–Ω–∏—Ü –∫–æ–¥–∞
// –°–≤–æ–π—Å—Ç–≤–æ unicodeScalar –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –Ω–∞–±–æ—Ä–∞ 21-–±–∏—Ç–Ω—ã—Ö —Å–∫–∞–ª—è—Ä–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π –Æ–Ω–∏–∫–æ–¥–∞, —á—Ç–æ —ç–∫–≤–∏–≤–∞–ª–µ–Ω—Ç–Ω–æ —Ñ–æ—Ä–º–µ –∫–æ–¥–∏—Ä–æ–≤–∞–Ω–∏—è UTF-32
let dogString = "Dog\u{203C}\u{1F436}"
for char in dogString {
    print(char, terminator: " ")
}
print("")

// MARK: –û—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ UTF-8
// –°–≤–æ–π—Å—Ç–≤–æ utf8 –∏–º–µ–µ—Ç —Ç–∏–ø String.UTF8View, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è–µ—Ç —Å–æ–±–æ–π –Ω–∞–±–æ—Ä –∑–Ω–∞–µ–Ω–∏–π unsigned 8-bit(UInt8), –ø–æ –æ–¥–Ω–æ–º—É –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –±–∞–π—Ç–∞ –≤ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–∏ UTF-8 —Å—Ç—Ä–æ–∫–∏:

for codeUnit in dogString.utf8 {
    print("\(codeUnit) ", terminator: " ")
}
print("")

// MARK: –û—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ UTF-16
// –°–≤–æ–π—Å—Ç–≤–æ utf16 –∏–º–µ–µ—Ç —Ç–∏–ø String.UTF16View, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è–µ—Ç —Å–æ–±–æ–π –Ω–∞–±–æ—Ä –∑–Ω–∞–µ–Ω–∏–π unsigned 16-bit(UInt16), –ø–æ –æ–¥–Ω–æ–º—É –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –±–∞–π—Ç–∞ –≤ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–∏ UTF-16 —Å—Ç—Ä–æ–∫–∏:
for codeUnit in dogString.utf16 {
    print("\(codeUnit)", terminator: " ")
}
print("")

// MARK: –û—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ —Å–∫–∞–ª—è—Ä–Ω—ã—Ö –≤–µ–ª–∏—á–∏–Ω –Æ–Ω–∏–∫–æ–¥–∞
// –° –ø–æ–º–æ—â—å—é —Å–≤–æ–π—Ç—Å–≤–∞ unicodeScalars –º–æ–∂–Ω–æ –ø–æ–ª—É—á–∏—Ç—å –¥–æ—Å—Ç—É–ø –∫ —Å–∫–∞–ª—è—Ä–Ω–æ–º—É –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏—é Unicode –∑–Ω–∞—á–µ–Ω–∏—è String. –≠—Ç–æ —Å–≤–æ–π—Å—Ç–≤–æ –∏–º–µ–µ—Ç —Ç–∏–ø UnicodeScalarView, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–µ–¥—Å—Ç–∞–≤—è–µ—Ç —Å–æ–±–æ–π –∫–æ–ª–ª–µ–∫—Ü–∏—é –∑–Ω–∞—á–µ–Ω–∏–π —Ç–∏–ø–∞ UnicodeScalar

// –ö–∞–∂–¥—ã–π UnicodeScalar –∏–º–µ–µ—Ç —Å–≤–æ–π—Ç—Å–≤–æ value, –∫–æ—Ç–æ—Ä–æ–µ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç 21-–±–∏—Ç–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Å–∫–∞–ª—è—Ä–∞, –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–Ω–æ–µ –≤ –ø—Ä–µ–¥–µ–ª–∞—Ö UInt32:

for scalar in dogString.unicodeScalars {
    print("\(scalar.value)", terminator: " ")
}
print("")

for scalar in dogString.unicodeScalars {
    print(scalar)
